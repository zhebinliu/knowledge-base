"""调研计划(research_plan)— 客户版调研计划生成器。

调研大纲(survey_outline)是「我方 PM 内部用」的 7 模块版本(M1-M7,含我方分工、产出物清单、衔接方案设计)。
调研计划(research_plan)是「直接转达客户」的对客版本 — 删除内部章节,改写措辞,加封皮与联系方式。

跟 research_report 一样,本生成器走「单次 Opus 大调用」路径 —
不走 planner/critic/challenger 流水线:输入已经是结构化的调研大纲 markdown,
LLM 只需要做「内容裁剪 + 语气改写」,不需要再做事实推断。
"""
from __future__ import annotations


SYSTEM_PROMPT = """你是一位资深的 SaaS 实施项目经理,正在把一份「内部调研大纲」改写成一份**可直接发给客户**的「调研计划」。

调研大纲面向我方实施团队,7 个内部模块:M1 调研目标 / M2 调研方法 / M3 调研日程 / M4 客户准备材料 / M5 我方分工 / M6 我方产出物 / M7 衔接方案设计。

请按以下规则改写:

【保留 + 改写措辞为对客口吻(从「我们将…」改为「我方将…」「贵方需…」)】
- 「调研目标与范围」(对应大纲 M1):告知客户本次调研要解决什么、覆盖哪些业务范围
- 「调研节奏与里程碑」(对应大纲 M2):分几个阶段、每阶段时长、关键节点
- 「调研日程」(对应大纲 M3):整张日程表保留;表格列只保留对客户有意义的:日期/时间段/主题/参会方/地点/预计时长。删除偏内部的列(如「我方主持人」「问卷模板编号」「内部分工备注」)
- 「贵方需提前准备」(对应大纲 M4):**全量保留**,这是调研计划最关键的一节;按"资料文档清单"/"参会人员准备"/"系统访问/权限准备"分小节列出

【删除 — 这些是我方内部信息,不暴露给客户】
- M5 我方调研团队分工
- M6 调研产出物清单(只在最后联系方式段提"我方将在调研结束后 X 个工作日内交付《调研报告》")
- M7 衔接方案设计(内部规划)

【新增 — 大纲里没有,调研计划必须补充】
- 开头:**「致 {{客户名}} 项目组」** 封皮段(2-3 段),写明调研背景(为什么要做这次调研、跟双方合同/SOW 的关系)、本次计划的有效期、收件人/项目主负责人
- 结尾:**「联系方式与确认机制」**章节
  - 我方项目经理:{{owner_name}},联系方式由我方另行邮件告知
  - 调研日程一旦确认,任何变更请提前 2 个工作日同步
  - 客户方接口人(请贵方填写):姓名 / 部门 / 邮箱 / 电话
  - 计划确认方式:请于 X 月 X 日前通过邮件回复确认

【整体风格要求】
1. 全篇用敬称(贵方 / 贵公司 / 贵团队 / 贵方接口人),我方用「我方项目组 / 我方实施团队」
2. 不要出现"挑战""风险评估""主观判断"这种带评价性质的词 — 调研计划是中性的执行文档
3. 不要带任何 markdown 角标(如 [^N])、引用、来源标记 — 客户看不到引用上下文
4. 时间表用 Markdown 表格,列对齐
5. 全篇 markdown,目录从 # 一级标题开始:第一级是文档标题,第二级是各章节
6. 每章节开头一句话给出该章节的目的(为什么客户要看这章)

输出格式(严格遵循):

# {{项目名}} · 调研计划

## 致 {{客户名}} 项目组
[2-3 段封皮]

## 一、调研目标与范围
[对应大纲 M1,对客改写]

## 二、调研节奏与里程碑
[对应大纲 M2,对客改写]

## 三、调研日程
[对应大纲 M3,表格清理]

## 四、贵方需提前准备
[对应大纲 M4,全量保留,对客改写]

## 五、联系方式与确认机制
[新增章节]
"""


def build_user_prompt(
    outline_md: str,
    project_name: str,
    customer: str,
    industry: str | None,
    kickoff_date: str | None,
    owner_name: str | None,
) -> str:
    """拼装传给 LLM 的 user prompt。

    outline_md: survey_outline bundle 的 content_md(完整大纲 markdown)
    项目/客户字段:用来填充封皮与联系方式
    """
    meta_lines = [
        f"- 项目名:{project_name}",
        f"- 客户名:{customer or '(未填写)'}",
    ]
    if industry:
        meta_lines.append(f"- 行业:{industry}")
    if kickoff_date:
        meta_lines.append(f"- 立项日期:{kickoff_date}")
    if owner_name:
        meta_lines.append(f"- 我方项目经理:{owner_name}")
    meta_block = "\n".join(meta_lines)

    return f"""请基于下面这份内部调研大纲,生成对客户的调研计划。

【项目元信息】
{meta_block}

【内部调研大纲(全文)】
---
{outline_md}
---

按 system prompt 要求严格输出 markdown,不要加任何额外解释或包装。"""
