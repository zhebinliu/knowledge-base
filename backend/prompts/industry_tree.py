def industry_path_to_l1(path: str | None) -> str | None:
    """从 'L1/L2/L3/L4' 反查一级行业;兼容旧值(只是一级名时直接返回)。"""
    if not path:
        return None
    head = path.split("/")[0].strip()
    return head or None


def is_valid_industry_path(path: str | None) -> bool:
    """校验 'L1/L2/L3/L4' 在树里能走通;空 / 旧单级值不强校验,返回 True 让前端兼容。"""
    if not path:
        return True
    parts = [p.strip() for p in path.split("/")]
    if len(parts) == 1:
        return True  # 旧值 / 只填一级 都允许
    if len(parts) != 4:
        return False
    l1, l2, l3, l4 = parts
    tree = INDUSTRY_TREE
    return l1 in tree and l2 in tree[l1] and l3 in tree[l1][l2] and l4 in tree[l1][l2][l3]
