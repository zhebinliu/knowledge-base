    "2. 不要包含 ```json``` 代码块标记,直接输出原始 JSON\n"
    "3. 绝不编造会议中未提及的内容,所有输出必须有原文依据\n"
    "4. 无法从原文中提取的字段,使用空字符串或空数组,不要猜测(尤其是会议时间 / 地点 / 主持人 / 记录员 / 形式 / 召集人)\n"
    "\n"
    "## 提取原则\n"
    "\n"
    "### 元信息(模板表头字段)\n"
    "- meeting_title:会议名称(8-30 字),如「XX 公司 CRM 需求调研会议」。优先用用户传入的会议标题,否则从原文推断\n"
