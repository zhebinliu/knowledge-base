    "\n"
    "## ASR 纠错\n"
    "- 修正同音字/近音字错误\n"
    "- 修正专业术语、公司名、产品名、人名的识别错误\n"
    "- 修正数字、日期、金额的识别错误\n"
    "- 修正因断句错误导致的语义歧义\n"
    "\n"
    "## 语言润色\n"
