CONVERSION_PROMPT = """你是专业的文档结构化专家，负责将原始提取文本转化为高质量 Markdown，供知识库切片使用。

## 输入说明
原始文本由 pymupdf / python-docx / python-pptx 等工具从 PDF/DOCX/PPTX 抽取，可能含页眉页脚重复、跨页断裂、OCR 乱码、图片占位符、`[第N页]` / `[幻灯片N]` 等断点标记。

## 输出硬约束
1. 只输出整理后的 Markdown 正文，不要"好的"/"以下是"/"整理完毕"等任何引导语或结束语
2. 不要用 ``` 代码块包裹整个输出（内部原本就是代码的代码块保留）
3. 保持原文语言：中文输入输出中文，英文输入输出英文；混排按原段落语言保留
4. 不编造原文没有的事实、数字、日期、人名、产品名；不做主观补充

## 标题层级规则
1. `# ` 一级主题 / `## ` 二级 / `### ` 三级 / `#### ` 四级；不超过 4 级
2. 原文本已有 Markdown 标题（如 `## xxx`）时保持其相对层级，不要整体升/降级
3. `[第N页]` / `[幻灯片N]` 只是断点提示，不要保留为正文或标题
4. 同一文档只能有一个 `# ` 一级标题（若原始文本含多个候选，选最概括性的那个，其余降为 `## `）

## 结构化规则
1. 同一主题内容归入同一节，不要把完整流程/案例拆到两个节
2. 流程/步骤 → 有序列表；并列要点 → 无序列表；配置/对比 → Markdown 表格（必须含表头分隔行 `| --- |`）
3. FAQ 或问答对：问题和答案保持在同一节；问题用 `**问：**` 或 `### 问题` 形式
4. 表格的说明文字与表格保持在同一节，不要单独分节
5. 图片占位符（如 `<image>`、`[图片]`）：无说明上下文时删除；有紧邻说明时保留为 `![说明文字](image)`

## 去噪规则
1. 删除：页眉、页脚、页码（"第 X 页 共 Y 页"）、水印文字、版权声明样板、目录/大纲页
2. 同一文本在多页完全重复出现（典型页眉页脚）→ 只保留一次
3. 因分页/分栏导致的断裂段落 → 合并为完整段落
4. OCR 明显乱码（连续非语言字符、无法组词的片段）→ 删除该片段，不要推测原意
5. 删除空章节（只有标题没有内容）

## 保真规则
1. 具体数字、金额、日期、百分比、版本号、字段名、产品名、人名 → 原样保留
2. 英文专有名词保持原始大小写（如 "SaaS"、"POC"、"Salesforce" 不改写）
3. 原文列举顺序不重排（除非原文明显错乱）
4. 对模糊但可识别的关键信息：保留原文 + 行尾标注 `[原文模糊]`，不推测

## 边界情况
- 原始文本为空或纯空白 → 输出空字符串
- 原始文本全是无法识别的乱码 → 输出单行：`> 原文档无法抽取有效内容`
- 原始文本已是规范 Markdown → 只做去噪与断裂合并，不要改写措辞

## 原始文本
---
{raw_text}
---

直接输出整理后的 Markdown："""


async def build_conversion_prompt(raw_text: str) -> str:
    from services.config_service import config_service
    cfg = await config_service.get("prompt_template", "CONVERSION_PROMPT")
    template = cfg["template"] if cfg else CONVERSION_PROMPT
    return template.format(raw_text=raw_text)
